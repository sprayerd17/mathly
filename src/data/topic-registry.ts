// lang defaults to 'en' for callers that haven't been updated to pass the
// active child's language yet — but every call site should pass it.
export function getTopics(grade: string, lang: 'en' | 'af' = 'en'): Topic[] {
  const topics = TOPICS_BY_GRADE[grade] ?? defaultTopics
  if (lang === 'en') return topics
  return topics.map(topic => ({ ...topic, name: topic.nameAf, description: topic.descriptionAf }))
}
